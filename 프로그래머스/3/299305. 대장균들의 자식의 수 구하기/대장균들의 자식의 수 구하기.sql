SELECT
DISTINCT(e1.ID)
, CASE
   WHEN e2.PARENT_ID IS NULL THEN 0
   ELSE COUNT(e1.ID)
   END AS CHILD_COUNT
FROM ECOLI_DATA e1
LEFT JOIN ECOLI_DATA e2
ON e1.ID = e2.PARENT_ID
GROUP BY e1.ID
ORDER BY 1











